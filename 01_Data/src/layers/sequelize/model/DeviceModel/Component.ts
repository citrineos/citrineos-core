import { IComponentDto, OCPP2_0_1, OCPP2_0_1_Namespace } from '@citrineos/base';
import {
  BelongsTo,
  BelongsToMany,
  Column,
  DataType,
  ForeignKey,
  Table,
} from 'sequelize-typescript';
import { Variable } from './Variable';
import { ComponentVariable } from './ComponentVariable';
import { BaseModelWithTenant } from '../BaseModelWithTenant';
import { Evse } from '../Location';

@Table({
  indexes: [
    {
      unique: true,
      fields: ['name'],
      where: {
        instance: null,
      },
    },
  ],
})
export class Component
  extends BaseModelWithTenant
  implements OCPP2_0_1.ComponentType, IComponentDto
{
  static readonly MODEL_NAME: string = OCPP2_0_1_Namespace.ComponentType;

  /**
   * Fields
   */

  @Column({
    type: DataType.STRING,
    unique: 'name_instance',
  })
  declare name: string;

  @Column({
    type: DataType.STRING,
    unique: 'name_instance',
  })
  declare instance?: string | null;

  /**
   * Relations
   */

  @BelongsTo(() => Evse)
  declare evse?: Evse;

  @ForeignKey(() => Evse)
  @Column(DataType.INTEGER)
  declare evseDatabaseId?: number | null;

  @BelongsToMany(() => Variable, () => ComponentVariable)
  declare variables?: Variable[];

  declare customData?: OCPP2_0_1.CustomDataType | null;

  // Declare the association methods, to be automatically generated by Sequelize at runtime
  public addVariable!: (variable: Variable) => Promise<void>;
  public getVariables!: () => Promise<Variable[]>;
}
